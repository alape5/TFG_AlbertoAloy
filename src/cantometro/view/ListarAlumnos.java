    public void populateTable(ArrayList<ArrayList> alumnos) {
        
        for(int i = 0; i < alumnos.size(); i++) {
            
            ArrayList a = alumnos.get(i);
            
            String nia = (String) a.get(2);
            String nombre = (String) a.get(0);
            String apellidos = (String) a.get(1);
            String curso = (String) a.get(3);
            String nota = (String) a.get(4);
            
            Object[] data = {nia, nombre, apellidos, curso, nota};
            dtm.addRow(data);
        }
        
    }

