    public void populateComboBoxAlgoritmos(ArrayList algos) {
        
        if(!algoLleno) {
            for(int i = 0; i < algos.size(); i++) {
                algoritmos_comboBox.addItem(algos.get(i).toString());
            }
            
            algoLleno = true;
        }
        
    }
    
    public void populateComboBoxInputs(ArrayList inputs) {
        if(!inputLleno) {
            for(int i = 0; i < inputs.size(); i++) {
                inputs_comboBox.addItem(inputs.get(i).toString());
            }
            
            inputLleno = true;
        }
    }
    
    public ArrayList getSettingsSeleccionados() {
        
        ArrayList newSettings = new ArrayList();
        
        String algo = this.algoritmos_comboBox.getSelectedItem().toString();
        String input = this.inputs_comboBox.getSelectedItem().toString();
        
        newSettings.add(algo);
        newSettings.add(input);
        
        return newSettings;
        
    }

